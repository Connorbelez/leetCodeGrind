class Solution:
    def isValidSudoku(self, board: List[List[str]]) -> bool:
        rows = dict(zip(list(range(9)), [set() for i in range(9)]))
        cols = dict(zip(list(range(9)), [set() for i in range(9)]))
        sqrs = {
            (0,0) : set(),
            (0,1) : set(),
            (0,2) : set(),
            (1,0) : set(),
            (1,1) : set(),
            (1,2) : set(),
            (2,0) : set(),
            (2,1) : set(),
            (2,2) : set(),
        }

        #get row
        for i in range(9):
            #get col in row
            for j in range(9):
                if board[i][j].isnumeric():
                    if board[i][j] in rows[i] or board[i][j] in cols[j]:
                        return False
                    else: 
                        rows[i].add(board[i][j])
                        cols[j].add(board[i][j])

                    curSq = (i//3,j//3)
                    if board[i][j] in sqrs[curSq]:
                        return False
                    else:
                        sqrs[curSq].add(board[i][j])
        return True
    
